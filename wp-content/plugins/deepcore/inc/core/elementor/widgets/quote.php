	/**
	 * Render Quote widget output on the frontend.
	 *
	 *
	 * @since 1.0.0
	 * @access protected
	 */
	protected function render() {
		$settings = $this->get_settings_for_display();
        // Class & ID 
        $shortcodeclass 	= $settings['shortcodeclass'] ? $settings['shortcodeclass'] : '';
        $shortcodeid		= $settings['shortcodeid'] ? ' id="' . $settings['shortcodeid'] . '"' : '';

        $custom_css = $settings['custom_css'];

		if ( $custom_css != '' ) {
			echo '<style>'. $custom_css .'</style>';
		}

		echo '<blockquote class="max-quote ' . $shortcodeclass . '"  ' . $shortcodeid . '	>';
        echo '<h2>'. $settings['qoute_content'] .'</h2>';
        echo '<cite>'. $settings['qoute_name'] .' <small>'. $settings['qoute_subname'] .'</small></cite></blockquote>';
	}

